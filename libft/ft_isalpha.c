#include "libft.h"

int	ft_isalpha(int c)
{
	return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}
int	ft_isuper(int c)
{
	return ((c >= 'A' && c <= 'Z'));
}
int	ft_islower(int c)
{
	return ((c >= 'a' && c <= 'z'));
}
